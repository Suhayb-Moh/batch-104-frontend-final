  const [cars, setCars] = useState([]);

  const { id } = useParams();
  useEffect(() => {
    axios
      .get(`http://localhost:8000/car/sortbyid/${id}`)
      .then((response) => {
        setCars(response.data.cars);
        console.log(response.data.cars);
      })
      .catch((error) => {
        console.log(error);
      });
  }, [id]);
